<h4><i class="fa-solid fa-bug-slash"></i> Safer, With No More Risks.</h4>
<hr>
<p>
Our proposed alternative is straightforward: it’s safer and doesn’t introduce any new risks. Unlike the old toxic wraps and bandages, the BPS is completely non-toxic, stable, and doesn’t fall under any dangerous chemical regulations. It fully seals the pole’s contact with the soil, stopping preservatives from leaking out, which is something the old wraps can’t do. By keeping the preservatives inside the pole, the BPS increases its durability and significantly reduces the chances of poles failing during storms, accidents, or general wear. It also allows for the use of safer preservatives, which cuts down on health risks, and most importantly, prevents harmful chemicals from leaching into the soil or contaminating groundwater.
</p>
<br>
<h4><i class="fa-solid fa-earth-americas"></i> Our goal is simple.</h4>
<hr>
<p>The BPS technology was specifically designed to address the growing need for an alternative to creosote, something that the proposer anticipated over 30 years ago. Unlike the older, almost impossible-to-remove wraps and bituminous bandages, the BPS doesn’t stick to the pole surfaces. This design feature allows for easy inspection of the poles, which is crucial for utilities around the world who regularly check their poles for wear and tear. The BPS has been tested and developed with various preservatives in different environments, proving its effectiveness in a wide range of conditions. For instance, when paired with oil-borne preservatives, BPS has been shown to perform better than untreated poles, as seen in vineyard tests where poles treated with creosote and BPS lasted much longer than identical poles without BPS in the same vineyards.

The real test, however, has been with utility poles, where the BPS has been in service for over 30 years. These utility poles are exposed to harsh conditions, including the intense decay that happens in tropical and subtropical climates, and yet the BPS-treated poles have outperformed non-BPS poles in durability. In fact, the CuNAP-treated version of the alternative has been successfully supplied to Seattle City Light, where it has been in service for over 26 years. The BPS has proven itself not just as a long-lasting solution but also as one that can stand up to some of the most aggressive environmental conditions, offering a reliable and sustainable alternative to creosote for pole preservation.</p>

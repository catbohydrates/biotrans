        last longer. The initial tests used prototypes on stakes, and the results showed the system was effective in preventing 
        decay. After this, the system was tested in Europe and in the tropical climate of Guadeloupe as part of an EU-funded project 
        that ran from 1998 to 2002. These trials confirmed that the BPS worked well in both temperate and tropical climates. By 1999, 
        the BPS had already shown good results on utility poles in Africa, where it helped keep poles in service longer and cut down on 
        maintenance. The testing didn’t end there. In 2004, a long-term trial was started in the United States by the 
        Electrical Power Research Institute (EPRI). This trial tracked how the BPS performed on utility poles over time to see if it could 
        offer a reliable, long-lasting solution to pole decay. Five years into the trial, the results still showed the system was working a
        s intended, preventing decay and helping poles last longer in different environments. These ongoing trials, with prototypes and 
        commercial versions, proved that the barrier protection system is a solid option for protecting utility poles and reducing 
        maintenance costs. The BPS has been tested in various climates, from tropical heat to colder, temperate zones, and it continues to 
        show that it can protect poles and extend their lifespan.</p>
